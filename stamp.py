#This program makes a pentagon with a turtle stamping
import turtle

blue1 = turtle.Turtle()
blue1.color("#6495ED")
blue1.shape("turtle")
for i in range(5):
    blue1.forward(100)
    blue1.left(72)
    blue1.stamp()


    
